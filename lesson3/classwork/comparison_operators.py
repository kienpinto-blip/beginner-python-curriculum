print(5 > 3)  # Greater than
print(5 < 3)  # Less than
print(5 >= 5)  # Greater than or equal to
print(5 <= 5)  # Less than or equal to
print(5 == 5)  # Equal to (note: double equals)
print(5 != 3)  # Not equal to

# You can compare variables too
a = 10
b = 7
print(a > b)
print(a == b)

x = int(input("Enter a number: "))
y = int(input("Enter a number: "))
print(x > y)

print("apple" == "apple")
print("Apple" == "apple")  # case matters when comparing strings
print("apple" != "banana")
